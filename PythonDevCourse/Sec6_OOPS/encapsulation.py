# Encapsulation is the binding of data and functions that manipulate that data.
# Imagine you have a toy robot. This robot can walk, talk, and light up. But you don’t need to know how all the wires,
# circuits, and computer parts inside the robot work to play with it. You just press a button, and the robot does what
# you want.

# Encapsulation in programming is like that robot. It means wrapping all the complicated parts of something, like how
# the robot works, inside a protective case so that you can use it without worrying about the details. You only see the
# buttons you need to press, and everything else is hidden.

# So, in simple terms, encapsulation is hiding the complicated stuff and showing only what’s necessary, making it easier
# and safer to use.
